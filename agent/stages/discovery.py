def _record_spa_host_signal(host: str, body_len: int) -> tuple[bool, int]:
    """Update the per-host SPA tracker with a new (host, body_len)
    observation. Returns (is_spa_host, repeat_count) — `is_spa_host`
    is True iff `host` has now been observed
    ≥ `_SPA_HOST_REPEAT_THRESHOLD` consecutive times with the same
    body length. Thread-safe.

    A non-matching length resets the counter for that host: a host
    that ALTERNATES between a 29KB SPA shell and a 5KB error page
    won't be flagged. Real SPAs serve byte-identical HTML at every
    path (the path is read from `window.location` at runtime), so
    exact-length match is the right gate.
    """
    if not host or body_len <= 0:
        return False, 0
    with _SPA_HOST_LOCK:
        prev_len = _SPA_HOST_BODY_LEN.get(host)
        if prev_len == body_len:
            count = _SPA_HOST_REPEAT_COUNT.get(host, 1) + 1
            _SPA_HOST_REPEAT_COUNT[host] = count
        else:
            _SPA_HOST_BODY_LEN[host] = body_len
            count = 1
            _SPA_HOST_REPEAT_COUNT[host] = count
        return count >= _SPA_HOST_REPEAT_THRESHOLD, count


def _should_escalate_for_spa(
    url: str, host: str, body_len: int,
) -> tuple[bool, str | None]:
    """Decide whether a candidate that would otherwise hit the hard-
    gate REJECT path should instead be escalated to Playwright on the
    basis of SPA / login-shape signals. Returns (should_escalate,
    reason).

    Side-effect: updates the per-host body-length tracker via
    `_record_spa_host_signal`.

    Three escalation signals (any one fires):
      (a) URL path contains a `_LOGIN_PATH_INDICATORS_SPA` token.
      (b) URL leftmost-subdomain is in `_LOGIN_SUBDOMAIN_LABELS_SPA`
          AND the path is a root shape.
      (c) Per-host SPA detected (≥ `_SPA_HOST_REPEAT_THRESHOLD` same-
          length bodies) AND signal (a) — i.e., a login-shaped path
          on a host known to serve identical SPA HTML at every path.

    (a) and (c) overlap (both require path-match); (c) is redundant
    by itself and exists as an explicit confidence boost so the log
    line records the SPA-host detection. The actual gate is (a) OR
    (b).
    """
    is_spa_host, repeat_count = _record_spa_host_signal(host, body_len)
    if not url:
        return False, None
    parsed = urlsplit(url)
    path = (parsed.path or "").lower()
    # Never escalate binary downloads to Playwright. Chromium kicks
    # off a Download for these and `page.goto` errors out with
    # "Download is starting", wasting a render slot. The pre-filter
    # / `_validate_one` extension check rejects most PDFs up-front,
    # but redirect targets (e.g. /upload/fees.PDF reached via 302)
    # can land here through a non-binary input URL.
    if path.endswith(FILE_EXTENSIONS):
        return False, None
    leftmost = host.split(".", 1)[0] if "." in host else host
    path_match = any(tok in path for tok in _LOGIN_PATH_INDICATORS_SPA)
    sub_match = leftmost in _LOGIN_SUBDOMAIN_LABELS_SPA
    root_path = (path.rstrip("/") or "/") in _LOGIN_SUBDOMAIN_ROOT_PATHS

    spa_suffix = (
        f" [spa-host {repeat_count}× len={body_len}]" if is_spa_host else ""
    )
    if path_match:
        return True, f"login-path indicator in {path!r}{spa_suffix}"
    if sub_match and root_path:
        return True, (
            f"login-subdomain {leftmost!r} at root path{spa_suffix}"
        )
    return False, None

# `<input ...>` followed by up to 200 chars of any character that contains
# either "login" or "sign in"; or "login"/"sign in" up to 200 chars before
# an `<input>` (for label-then-input layouts).
_INPUT_NEAR_LOGIN_RE = re.compile(
    r"(?:<input\b[^>]*>[\s\S]{0,200}?(?:login|sign\s*in)"
    r"|(?:login|sign\s*in)[\s\S]{0,200}?<input\b)",
    re.IGNORECASE,
)


def _dns_resolves_with_timeout(host: str, timeout: float = _HARD_GATE_DNS_TIMEOUT) -> tuple[bool, str]:
    """Resolve `host` to an IP via DNS with a hard wall-clock cap.

    `socket.gethostbyname` itself has no timeout knob and will block on the
    OS resolver. Run it in a daemon thread and join with `timeout`; if the
    thread hasn't returned by then, treat as DNS-failure. Avoids the
    process-global `socket.setdefaulttimeout`, which is not thread-safe.
    """
    if not host:
        return False, "empty host"
    err: list[str] = []

    def _resolve() -> None:
        try:
            socket.gethostbyname(host)
        except Exception as e:  # noqa: BLE001 — surface error class+msg
            err.append(f"{type(e).__name__}: {e}")

    t = threading.Thread(target=_resolve, daemon=True)
    t.start()
    t.join(timeout)
    if t.is_alive():
        return False, f"DNS timeout (>{timeout}s)"
    if err:
        return False, f"DNS: {err[0]}"
    return True, ""


_CONTENT_FINGERPRINT_BYTES: int = 200


def _content_fingerprint(
    url: str, *, http_timeout: float, user_agent: str,
) -> str | None:
    """MD5 hash of the first ``_CONTENT_FINGERPRINT_BYTES`` of the
    response body. Used to compare a wildcard-DNS canary's response
    to a candidate subdomain's response — matching fingerprints mean
    "this subdomain returns the same generic wildcard page". Returns
    None when the fetch fails (network error, non-200, etc.).
    """
    if not url:
        return None
    try:
        resp = discovery_rules.HTTP_SESSION.get(
            url,
            headers={"User-Agent": user_agent},
            timeout=http_timeout,
            allow_redirects=True,
            stream=True,
        )
        # Only read the prefix; close immediately so the connection
        # returns to the pool without buffering the full body.
        body = resp.content[:_CONTENT_FINGERPRINT_BYTES]
        resp.close()
    except Exception:
        return None
    if not body:
        return None
    import hashlib
    return hashlib.md5(body).hexdigest()


def _has_wildcard_dns(
    host: str,
    *,
    http_timeout: float = _HARD_GATE_DNS_TIMEOUT,
    user_agent: str = "",
) -> tuple[bool, str | None]:
    """Detect wildcard-DNS hosts and capture the wildcard response
    fingerprint so callers can distinguish real subdomains from
    wildcard-routed fakes.

    Some Indian-uni / SaaS hosts (mitapps.in, vnsguapps.in, …) are
    configured with a wildcard A-record so EVERY subdomain resolves
    to a single IP. The naive "is wildcard? → skip all subdomains"
    rule blocks legitimate portals (`student.mitapps.in`,
    `oes.mitapps.in`) along with the noise. The fingerprint lets the
    caller compare each real subdomain's content against the canary
    response — match → fake (skip), differ → real (keep).

    Returns ``(is_wildcard, fingerprint)``:
      * ``(False, None)`` — DNS doesn't resolve a canary subdomain;
        host has normal DNS, no special handling needed.
      * ``(True, str)`` — wildcard detected; ``str`` is MD5 of the
        canary's first ``_CONTENT_FINGERPRINT_BYTES`` of body. The
        canary URL fetched via HTTPS to GET the wildcard's response.
      * ``(True, None)`` — wildcard DNS confirmed but the HTTP fetch
        failed (timeout, TLS error, connection reset). Caller should
        treat this as "wildcard but fingerprint unknown" and fall
        back to the conservative behavior (skip all subdomains).

    Bias: DNS lookup timeout/error returns ``(False, None)`` so a
    slow-resolver host doesn't get its legitimate probes silently
    dropped.
    """
    if not host:
        return False, None
    h = host.lower().lstrip(".").split(":")[0]
    canary_prefix = f"__reclaim-canary-{uuid.uuid4().hex[:12]}__"
    canary = f"{canary_prefix}.{h}"
    ok, _reason = _dns_resolves_with_timeout(canary, timeout=http_timeout)
    if not ok:
        return False, None
    fingerprint = _content_fingerprint(
        f"https://{canary}/",
        http_timeout=http_timeout,
        user_agent=user_agent or "Mozilla/5.0",
    )
    return True, fingerprint


# Process-wide cache of (registrable root → (is_wildcard, fingerprint))
# for rule-C-bypass validation. Computed lazily on the first rule-C accept
# for a root; reused across OrgIDs in the same process. Keeps the wildcard
# check effectively free after the first hit per root.
_wildcard_canary_cache: dict[str, tuple[bool, str | None]] = {}
_wildcard_canary_cache_lock = threading.Lock()


def _wildcard_canary_for_root(
    root: str, *, http_timeout: float, user_agent: str,
) -> tuple[bool, str | None]:
    """Cached wrapper around `_has_wildcard_dns` keyed by registrable root.

    Returns ``(is_wildcard, fingerprint)`` — same shape as `_has_wildcard_dns`.
    Thread-safe; first caller per root pays the DNS+HTTP cost, subsequent
    callers read from cache.
    """
    if not root:
        return False, None
    with _wildcard_canary_cache_lock:
        cached = _wildcard_canary_cache.get(root)
        if cached is not None:
            return cached
    result = _has_wildcard_dns(
        root, http_timeout=http_timeout, user_agent=user_agent,
    )
    with _wildcard_canary_cache_lock:
        _wildcard_canary_cache[root] = result
    return result


def _body_fingerprint(body: str) -> str | None:
    """MD5 of the first ``_CONTENT_FINGERPRINT_BYTES`` of ``body`` encoded as
    UTF-8. Mirrors `_content_fingerprint`'s bytewise hash so a validated
    response body can be compared directly against a wildcard-DNS canary
    fingerprint captured via the same prefix-of-bytes path."""
    if not body:
        return None
    raw = body.encode("utf-8", errors="replace")[:_CONTENT_FINGERPRINT_BYTES]
    if not raw:
        return None
    import hashlib
    return hashlib.md5(raw).hexdigest()


def _body_has_interactive_form(body: str) -> bool:
    """True if the body looks like a real interactive page — has any of:
      * `<form>` tag
      * password input (`_PASSWORD_INPUT_RE`)
      * an `<input>` element with "login" / "sign in" within ~200 chars
    """
    if not body:
        return False
    if _PASSWORD_INPUT_RE.search(body):
        return True
    body_lower = body.lower()
    if "<form" in body_lower:
        return True
    if _INPUT_NEAR_LOGIN_RE.search(body):
        return True
    return False


def _is_bare_platform_root(final_url: str) -> tuple[bool, str]:
    """True if `final_url` is the bare host of a known shared platform —
    i.e. discovery resolved to `https://samarth.edu.in/` instead of staying
    on an institutional subdomain."""
    p = urlsplit(final_url or "")
    host = p.netloc.lower().split(":")[0]
    if not host:
        return False, ""
    if host in KNOWN_SHARED_PLATFORM_PATTERNS:
        return True, f"redirected to bare platform root {host}"
    return False, ""



# --- Hard verification gate ----------------------------------------------
#
# Every URL written to the sheet has to clear this gate, regardless of how
# it was discovered. Motivation: Stage A previously fabricated URLs by
# guessing platform-tenant subdomains (e.g. `<shortname>.samarth.edu.in`);
# many of those returned a 200 from a wildcard / index page and slipped
# through the looser checks. The gate enforces:
#
#   1. DNS resolves (3s timeout)             — `_dns_resolves_with_timeout`
#   2. HTTP final status ∈ {200, 401, 403}   — `_HARD_GATE_OK_STATUSES`
#   3. Body length > 1000 chars
#   4. Body has <form>, password input, OR an <input> with login/sign-in
#      text within ~200 chars                — `_body_has_interactive_form`
#   5. Final URL is not the bare platform root (samarth.edu.in/, …)
#
# Empty result is acceptable. If verification fails the URL is dropped and
# logged at WARNING.
_HARD_GATE_OK_STATUSES: frozenset[int] = frozenset({200, 401, 403})
_HARD_GATE_REJECT_STATUSES: frozenset[int] = frozenset({404, 410})
_HARD_GATE_MIN_BODY_LEN: int = 1000
_HARD_GATE_DNS_TIMEOUT: float = 3.0


# --- Per-host SPA detection -------------------------------------------------
#
# SPA shells (React / Angular / Vue) serve identical hydrated HTML at every
# path — the static body has no `<form>` tag, so the existing hard gate
# rejects every candidate on the host. `js_shell_suspicion_score` only
# fires its strong (+3) signals when the body is < 2000 chars; medium-
# size SPA shells (Patliputra's `ppup.ac.in/*` returns 29KB at every
# path) score below the threshold and never escalate to Playwright.
#
# This module-level tracker counts how many distinct candidates on each
# host have returned bodies of the same byte length. After
# `_SPA_HOST_REPEAT_THRESHOLD` matches, the host is "known SPA" — and
# `_should_escalate_for_spa` returns True for login-shaped URLs on it
# regardless of the suspicion-score outcome. Path / subdomain login
# indicators trigger escalation on a single signal too, so a single
# obviously-login URL doesn't have to wait for 3 same-length siblings.
#
# The tracker is module-level (persists across OrgIDs in a single run);
# SPA-ness is a property of the host, not the OrgID. Concurrency:
# `_validate_one` runs on a `ThreadPoolExecutor`, so increments must
# be serialised — `_SPA_HOST_LOCK` makes the read-modify-write atomic.
# Eventual detection is acceptable: some early candidates may be
# rejected before the counter crosses the threshold, but later
# candidates on the same host will trigger.
_SPA_HOST_REPEAT_THRESHOLD: int = 3
_SPA_HOST_BODY_LEN: dict[str, int] = {}
_SPA_HOST_REPEAT_COUNT: dict[str, int] = {}
_SPA_HOST_LOCK = threading.Lock()


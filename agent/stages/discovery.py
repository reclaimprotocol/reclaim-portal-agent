def _is_never_probe_domain(domain: str) -> bool:
    """True iff `domain` equals or is a subdomain of any
    `NEVER_PROBE_DOMAINS` entry."""
    if not domain:
        return False
    d = domain.lower().lstrip(".")
    for plat in NEVER_PROBE_DOMAINS:
        if d == plat or d.endswith("." + plat):
            return True
    return False


